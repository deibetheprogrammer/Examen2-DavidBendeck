    private void B_IniciarSesion_LogInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_B_IniciarSesion_LogInMouseClicked
        // TODO add your handling code here:
        
        String ID = TF_ID_LogIn.getText();
        String clave = PF_Clave_LogIn.getText();
        Usuario user = null;
        
        for (Usuario usuario : usuarios) {
            if (usuario.getId().equals(ID)) {
                if (usuario.getClave().equals(clave)) {
                    user = usuario;
                }
            }
        }
        if (user == null) {
            contador++;
            if (contador == 5) {
                JOptionPane.showMessageDialog(D_LogIn, "Ha ingresado un ID y/o contrase√±a incorrecto(s) cinco veces consecutivas");
                guardarLog("ALERTA: Intento fallido de ingreso cinco veces consecutivas, ATM: " + atmActual.getAtmId());
                D_LogIn.setVisible(false);
            }
            JOptionPane.showMessageDialog(D_LogIn, "ID y/o clave incorrecto(s)");
        }
        
        
        else if (user instanceof Cliente) {
            clienteActual = (Cliente) user;
            
            guardarLog("Inicio de sesi√≥n, cliente: " + clienteActual.getId() + ", ATM: " + atmActual.getAtmId());
            
            D_LogIn.setVisible(false);
            
            D_MenuCliente.pack();
            D_MenuCliente.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
            D_MenuCliente.setVisible(true);
            
            if (transRecibo != null) {
                int resp = JOptionPane.showConfirmDialog(D_LogIn, "¬øDesea imprimir su recibo?");
                
                if (resp == 0) {
                    String factura = "fecha: " + transRecibo.getFechaHora().toString().replace("T", " ").substring(0, 19) + "\n"
                            + "Cajero: " + atmActual.getAtmId() + "\n"
                            + "Ubicacion: " + atmActual.getUbicacion() + "\n"
                            + "Cliente: " + clienteActual.getNombre1() + " " + clienteActual.getApellido1() + "\n"
                            + "\n"
                            + transRecibo.getDescripcion() + "\n\n"
                            + "Gracias por su visita";
                    JOptionPane.showMessageDialog(D_LogIn,factura);
                    transRecibo = null;
                }
            }
        } 
        
        else {
            mantenimientoActual = (UsuarioMantenimiento) user;
            
            if (!mantenimientoActual.getAtms().contains(atmActual.getAtmId())) {
                JOptionPane.showMessageDialog(D_LogIn, "ALERTA: Este ATM no est√° asignado al usuario de Mantenimiento ingresado");
                guardarLog("Usuario: " + mantenimientoActual + " intent√≥ accesar cajero: " + atmActual + " que no tiene asignado");
                return;
            }
            
            guardarLog("Inicio de sesi√≥n, Usuario de mantenimiento: " + mantenimientoActual.getId() + ", ATM: " + atmActual.getAtmId());
            
            D_LogIn.setVisible(false);
            
            D_MenuMantenimiento.pack();
            D_MenuMantenimiento.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
            D_MenuMantenimiento.setVisible(true);
        }
    }//GEN-LAST:event_B_IniciarSesion_LogInMouseClicked

