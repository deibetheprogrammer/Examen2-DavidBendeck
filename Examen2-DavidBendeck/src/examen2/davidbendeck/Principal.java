    private void B_CrearUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_B_CrearUsuarioMouseClicked
        // TODO add your handling code here:
        
        String ID = TF_ID_CrearUsuario.getText();
        String nombre1 = TF_Nombre1_CrearUsuario.getText();
        String nombre2 = TF_Nombre2_CrearUsuario.getText();
        String apellido1 = TF_Apellido1_CrearUsuario.getText();
        String apellido2 = TF_Apellido2_CrearUsuario.getText();
        String clave = PF_Clave_CrearUsuario.getText();
        int nacimiento = Integer.parseInt(TF_Nacimiento_CrearUsuario.getText());
        int afiliacion = Integer.parseInt(TF_Afiliacion_CrearUsuario.getText());
        
        for (Usuario usuario : usuarios) {
            if (usuario.getId().equals(ID)) {
                JOptionPane.showMessageDialog(this, "Este ID ya existe");
                return;
            }
        }
        
        String tipoUsuario = (String) CB_TipoUsuario_CrearUsuario.getSelectedItem();
        if (tipoUsuario.equals("Usuario Cliente")) {
            usuarios.add(new Cliente(new ArrayList<Cuenta>(), new ArrayList<Transaccion>(), ID, nombre1, nombre2, apellido1, apellido2, clave, nacimiento, afiliacion));
        } else {
            usuarios.add(new UsuarioMantenimiento(new ArrayList<String>(), ID, nombre1, nombre2, apellido1, apellido2, clave, nacimiento, afiliacion));
        }
        
        guardarUsuarios(usuarios);
        JOptionPane.showMessageDialog(this, "Se ha creado un nuevo usuario con exito");
    }//GEN-LAST:event_B_CrearUsuarioMouseClicked

    private void B_CrearATM_CrearATMsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_B_CrearATM_CrearATMsMouseClicked
        // TODO add your handling code here:
        
        String ID = TF_ID_CrearATM.getText();
        String ubicacion = TF_Ubicacion_CrearATM.getText();
        int fabricacion = Integer.parseInt(TF_Fabricacion_CrearATM.getText());
        int mantenimiento = Integer.parseInt(TF_Mantenimiento_CrearATM.getText());
        int billetes100 = Integer.parseInt(TF_Billetes100_CrearATM.getText());
        int billetes500 = Integer.parseInt(TF_Billetes500_CrearATM.getText());
        
        if (ID == null) {
            JOptionPane.showMessageDialog(this, "Escriba un ID valido por favor");
        }
        
        for (ATM atm : atms) {
            if (atm.getAtmId().equals(ID)) {
                JOptionPane.showMessageDialog(this, "Este ID ya existe");
            }
        }
        
        atms.add(new ATM(ID, ubicacion, fabricacion, mantenimiento, billetes100, billetes500));
        guardarATMs(atms);
        
        JOptionPane.showMessageDialog(this, "ATM guardado exitosamente");
    }//GEN-LAST:event_B_CrearATM_CrearATMsMouseClicked

